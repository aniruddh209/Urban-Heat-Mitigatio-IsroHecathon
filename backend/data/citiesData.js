function getCityByName(name) { return INDIAN_CITIES.find(c => c.name.toLowerCase() === name.toLowerCase()) || null; }
function getCitiesByState(state) { return INDIAN_CITIES.filter(c => c.state.toLowerCase() === state.toLowerCase()); }
function getTopRiskCities(n = 20) { return [...INDIAN_CITIES].sort((a, b) => b.risk_score - a.risk_score).slice(0, n); }
function getVulnerabilitySummary() {
  const levels = { Critical: 0, High: 0, Moderate: 0, Low: 0 };
  INDIAN_CITIES.forEach(c => { levels[c.vulnerability] = (levels[c.vulnerability] || 0) + 1; });
  return levels;
}

